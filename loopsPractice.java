public class loopsPractice {
    public static void main(String[] Args){
        for (int i = 1; i<=10; i++){
            System.out.println(i);
        };
        System.out.println(" ");
        int i = 1;
        while(i<=10){
            System.out.println(i);
            i++;
        };
        System.out.println(" ");  
        for(i = 10; i<=100; i+= 10){
            System.out.println(i);
        };
        System.out.println(" ");
        i=10;
        while(i<=100){
            System.out.println(i);
            i+=10;
        };
        System.out.println(" ");
        for(i=10;i>0;i--){
            System.out.println(i);
        };
        System.out.println(" ");
        i=10;
        while(i>0){
            System.out.println(i);
            i--;
        }
    }
}
